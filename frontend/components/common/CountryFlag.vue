<template>
  <img
    :class="['CountryFlag', {small, isGlobal}]"
    :src="countryFlagUrl"
  >
</template>

<script>
export default {
  props: {
    code: {
      type: String,
      default: null
    },
    small: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    countryFlagUrl () {
      if (this.isGlobal) {
        return '/who-logo-small.svg';
      }
      if (this.code) {
        return `/static/flags/${this.code.toLowerCase()}.png`;
      }
      return null;
    },
    isGlobal () {
      return this.code === 'NULL';
    }
  }
};
</script>

<style lang="less">
  @import "../../assets/style/variables.less";
  @import "../../assets/style/mixins.less";

  .CountryFlag {
    &.small {
      height: 16px;
      width: 24px;
    }
    &.isGlobal {
      box-shadow: none !important;
      height: 20px !important;
      width: 24px !important;
      margin-top: -4px;
    }
  }
</style>
